class CarCareData {
  final String plate;
  final String brand;
  final String oil;
  final String battery;
  final String tire;
  final String brakes;
  final String airFilter;
  final String spark;

  CarCareData({
    required this.plate,
    required this.brand,
    required this.oil,
    required this.battery,
    required this.tire,
    required this.brakes,
    required this.airFilter,
    required this.spark,
  });
}
